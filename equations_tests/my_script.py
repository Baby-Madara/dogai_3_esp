class fk_bp:
    def __init__(self, Xbs, Ybs, Zbs, Ysl, Xlf, Xfp):
        self.Xbs = Xbs
        self.Ybs = Ybs
        self.Zbs = Zbs
        self.Ysl = Ysl
        self.Xlf = Xlf
        self.Xfp = Xfp

    def calc_xyz(self, thetaS, thetaL, thetaF):
        thetaS = thetaS * (np.pi / 180.0)
        thetaL = thetaL * (np.pi / 180.0)
        thetaF = thetaF * (np.pi / 180.0)
        return np.array([
            [np.sin(thetaF + thetaL), np.cos(thetaF + thetaL), 0,
             self.Xbs + self.Xfp * np.sin(thetaF + thetaL) + self.Xlf * np.sin(thetaL)],
            [np.cos(thetaF + thetaL) * np.sin(thetaS), -np.sin(thetaF + thetaL) * np.sin(thetaS), -np.cos(thetaS),
             self.Ybs + self.Ysl * np.cos(thetaS) + self.Xfp * np.cos(thetaF + thetaL) * np.sin(thetaS) + self.Xlf * np.cos(thetaL) * np.sin(thetaS)],
            [-np.cos(thetaF + thetaL) * np.cos(thetaS), np.sin(thetaF + thetaL) * np.cos(thetaS), -np.sin(thetaS),
             self.Zbs + self.Ysl * np.sin(thetaS) - self.Xfp * np.cos(thetaF + thetaL) * np.cos(thetaS) - self.Xlf * np.cos(thetaL) * np.cos(thetaS)],
            [0, 0, 0, 1]
        ])

