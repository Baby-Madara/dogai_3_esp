# Function to parse the text data and convert to XYZ positions
def parse_and_convert(text_data):
    lines = text_data.strip().split('\n')
    results = []

    for line in lines:
        if '|' in line:
            [leg, parts] = line.split('|')
            # print(leg)
            # print(parts)
            angles = {}
            # for part in parts:
            try:
                # leg, data = part.split(',')
                S, L, F = map(float, parts.split(','))
                angles[leg.strip()] = (S, L, F)
            except ValueError as e:
                print(f"Skipping part due to error: {part} -> {e}")
                continue

            xyz_positions = []
            for leg, (S, L, F) in angles.items():
                xyz = fk_objects[leg].calc_xyz(S, L, F)
                xyz_positions.append(xyz[:3, 3])  # Extract the XYZ coordinates from the transformation matrix

            results.append(xyz_positions)

    return results

# Function to save XYZ positions to CSV
def save_to_csv(xyz_data, filename='output.csv'):
    header = ['frx', 'fry', 'frz', 'flx', 'fly', 'flz', 'brx', 'bry', 'brz', 'blx', 'bly', 'blz']
    with open(filename, mode='w', newline='') as file:
        writer = csv.writer(file)
        writer.writerow(header[0:3])
        writer.writerow(header[0:3])
        writer.writerow(header[3:6])
        writer.writerow(header[6:9])
        writer.writerow(header[9:])
        for data in xyz_data:
            row = []
            for pos in data:
                row.extend(pos)
            
            writer.writerow(row)

